# 1. დამატება (+)
a = 10
b = 5
result = a + b
print("1. a + b =", result)

# 2. გამოკლება (-)
result = a - b
print("2. a - b =", result)

# 3. გამრავლება (*)
result = a * b
print("3. a * b =", result)

# 4. გაყოფა (/)
result = a / b
print("4. a / b =", result)

# 5. მთელი რიცხვით გაყოფა (//)
result = a // b
print("5. a // b =", result)

# 6. ნაშთის გამოთვლა (%)
result = a % b
print("6. a % b =", result)

# 7. ხარისხში აყვანა (**)
result = a ** b
print("7. a ** b =", result)

# 8. გამოკლების შედეგის შემცირება (-=)
a -= 2
print("8. a -= 2 -> a =", a)

# 9. დამატების შედეგის გაზრდა (+=)
a += 4
print("9. a += 4 -> a =", a)

# 10. გამრავლების შედეგის შემცირება (*=)
a *= 2
print("10. a *= 2 -> a =", a)